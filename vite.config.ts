// Polyfill crypto.hash for Node 18 (added in Node 20.12+)
import crypto, { createHash } from "node:crypto";
if (!(crypto as any).hash) {
  (crypto as any).hash = (alg: string, data: string | Buffer, enc?: string) =>
    createHash(alg).update(data).digest((enc ?? "hex") as import("node:crypto").BinaryToTextEncoding);
}

